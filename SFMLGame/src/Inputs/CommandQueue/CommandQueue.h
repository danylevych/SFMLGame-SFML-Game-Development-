#pragma once

#include <queue>
#include "../Command/Command.h"


//////////////////////////////////////////
// \brief
//		Class stores the queue of the
//		Commands in our game.
// 
//////////////////////////////////////////
class CommandQueue
{
private:
	std::queue<Command> queue;

public:
	//////////////////////////////////////////
	// \brief
	//		Insert the value into queue.
	// 
	//////////////////////////////////////////
	void Push(const Command& command);
	
	//////////////////////////////////////////
	// \brief
	//		Returns the first element in
	//		query and remove it.
	// 
	//////////////////////////////////////////
	Command Pop();

	//////////////////////////////////////////
	// \brief
	//		Return true when our queue is
	//		empty, otherwise - false.
	// 
	//////////////////////////////////////////
	bool IsEmpty() const;
};

